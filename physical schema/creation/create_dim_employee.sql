CREATE TABLE dim_employee (
    employee_key INT,
    employee_id INT,
    employee_first_name VARCHAR2(255),
    employee_last_name VARCHAR2(255),
    employee_email VARCHAR2(255),
    employee_phone_number VARCHAR2(20),
    employee_job_title VARCHAR2(255),
    employee_salary NUMBER(10,2),
    employee_hiring_date DATE
);
